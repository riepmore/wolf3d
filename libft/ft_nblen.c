#include "libft.h"

int			ft_nblen(int n)
{
	int			i;

	i = 1;
	if (n < 0)
	{
		i++;
		n = n - (n * 2);
	}
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	return (i);
}
