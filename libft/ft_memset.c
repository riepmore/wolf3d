#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t		i;

	i = 0;
	if (b != NULL)
	{
		while (i != len)
		{
			*((char *)b + i) = c;
			i++;
		}
		return (b);
	}
	return (NULL);
}
