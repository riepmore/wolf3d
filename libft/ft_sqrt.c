#include "libft.h"

double		ft_sqrt(double x)
{
	double		c;
	double		diff;

	c = 0.0;
	if (x == 0.0 || x == 1.0)
		return (c);
	c = x;
	diff = c;
	c = 0.5 * (c + x / c);
	while (c != diff)
	{
		diff = c;
		c = 0.5 * (c + x / c);
	}
	return (c);
}
